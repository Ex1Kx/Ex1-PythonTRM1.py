class ciclo:
    def __init__(self, inicio, final):
        self.inicio = inicio
        self.final = final

def imprimirnumero(self):
    while self.inicio < self.final:
        print(f"{self.inicio}")
        self.inicio += 1

objetonumero = ciclo(int(input("inicio: ")), int(input("hasta: ")))
objetonumero.imprimirnumero()